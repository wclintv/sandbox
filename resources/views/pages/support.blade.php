				<br>
				<p><strong>Q) How do I add a user?</strong></p>
				<p>A) If you are the "Admin" user in your QuickBooks Online account then, when you login to SnapDsk, you will see a gear icon in the upper right corner where you can "invite"
				      existing Intuit accounts to join your SnapDsk instance.  When you invite the new user to join SnapDsk they will recieve an email.  Once they accept the invitation in the email,
					  they will get access to your customer data.</p>
				<br>		  
				<p><strong>Q) What if I'm not the "Admin" user of my companies QuickBooks account, can I still join?</strong></p>
				<p>A) Anyone with an Intuit userID and password can login to SnapDsk but until you're invited by the QuickBooks Admin User, you won't be able to view any customer information.</p>
				<br>
				<p><strong>Q) How long will it take for invitation emails to be sent to new users that I want to add?</strong></p>
				<p>A) It may take anywhere from 2 - 5 minutes for invitation emails to be sent to new users being setup by the Admin user.  If after 5 minutes the 
				      invitation email has not been recieved then re-send the invitation.  Microsoft email accounts (i.e. Outlook, Hotmail, etc.) have a particular issue 
					  at this time and may need to be re-sent.</p>
				<br>
				<p><strong>Q) If I mark a customer as "inactive" in QuickBooks it will still appear in SnapDsk?</strong></p>
				<p>A) Yes, but the customer can no longer be updated in QuickBooks from SnapDsk.</p>
				<br>
				<p><strong>Q) What happens if there is ever a synchronization conflict between SnapDsk and QuickBooks?</strong></p>
				<p>A) This is unlikely but in that case, SnapDsk will default to the QuickBooks data.</p>
				<br>
				<p><strong>Q) Why isn't the 'Same as billing address' box checked in QuickBooks?</strong></p>
				<p>A) If the 'Same as billing address' option is selected in SnapDsk, the QuickBooks check box will not reflect this, but the data will be correct.</p>
