				<p><strong>Q) How do I update my user profile?</strong></p>
				<p>A) SnapDsk gets all of its user profile information from your Intuit account.  If you need to make an update, login to your Intuit account and make the change there;
				      it will then be updated in your SnapDsk profile.</p>
